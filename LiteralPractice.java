public class LiteralPractice {

    public static void main(String[] args) {

        long largeNumber = 9876543210L;

        float decimalNumber = 12.75f;

        char copyrightSymbol = '\u00A9';

        System.out.println("=== Literal Practice ===");
        System.out.println("Long value: " + largeNumber);
        System.out.println("Float value: " + decimalNumber);
        System.out.println("Char (Unicode) value: " + copyrightSymbol);
    }
}